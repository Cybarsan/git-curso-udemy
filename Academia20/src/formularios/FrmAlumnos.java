    private void grabaAlumno() {
        em.getTransaction().begin();
        Alumnos alum = new Alumnos();
        alum.setNombreAlumno(txtNombres.getText().trim());
        alum.setApellidoAlumno(txtApellidos.getText().trim());
        alum.setCelular1(txtEmail.getText().trim());
        alum.setMail(txtCelular.getText().trim());
        alum.setCedulaAlumno(txtCedula.getText().trim());
        alum.setCelular2(txtCelular2.getText().trim());
        alum.setDireccion(txtDireccion.getText().trim());
        alum.setActivo(1);
        Date FecNac = null;
        if (txtFechaNacimiento.getText() != "null") {

            try {
                FecNac = formatofec.parse(txtFechaNacimiento.getText());
            } catch (Exception e) {
                Logger.getLogger(FrmAlumnos.class.getName()).log(Level.SEVERE, null, e);
            }
        }
        alum.setFechanac(FecNac);

        em.persist(alum);
        em.getTransaction().commit();
        jBtnGuardar.setEnabled(false);
        JOptionPane.showMessageDialog(this, "Guardado con Éxito!!!");
        defaultTableModelSocio();
        cargatablaAlumno();

    }

