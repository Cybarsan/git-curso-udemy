    private void cargaParientes() {
        tx = em.getTransaction();
        tx.begin();
        Parientes par = em.find(Parientes.class, al.getIdalumno());
        Query q = em.createQuery("select p from Parientes p where p.idalumnos = :ALUMNO");
        if (par != null) {
            q.setParameter("ALUMNO", par.getIdalumnos());
            //aca meter if

            List<Parientes> listParientes = q.getResultList();
            for (Iterator<Parientes> it = listParientes.iterator(); it.hasNext();) {
                Parientes pari = it.next();
                modelParientes.addRow(new Object[]{pari.getIdparientes(), pari.getNrocedula(), pari.getNombres(), pari.getApellidos()});
            }
        }
        tx.commit();

    }

    private void cargatablaAlumno() {
        tx = em.getTransaction();
        tx.begin();

        List<Alumnos> lista = em.createQuery("select a from Alumnos a WHERE a.activo = 1 order by a.idalumno").getResultList();

        for (Iterator<Alumnos> it = lista.iterator(); it.hasNext();) {
            Alumnos alum = it.next();
            String grabai = null;
            fecnac = alum.getFechanac();
            if (fecnac != null) {
                //JOptionPane.showMessageDialog(null, fecnac);
                String diai, mesi, anioi;
                diai = String.format("%02d", (fecnac.getDate()));
                mesi = String.format("%02d", (fecnac.getMonth()) + 1);
                anioi = String.valueOf(fecnac.getYear() + 1900);
                grabai = diai + "/" + mesi + "/" + anioi;
            } else {
                fecnac = new Date();
                String diai, mesi, anioi;
                diai = String.format("%02d", (fecnac.getDate()));
                mesi = String.format("%02d", (fecnac.getMonth()) + 1);
                anioi = String.valueOf(fecnac.getYear() + 1900);
                grabai = diai + "/" + mesi + "/" + anioi;
            }
            //System.out.print(grabai);
            Integer ant = edad(grabai);
            String naci = null;
            if (alum.getFechanac() != null) {
                naci = formatofec.format(alum.getFechanac());
            }

            modelAlumno.addRow(new Object[]{alum.getIdalumno(), alum.getCedulaAlumno(), alum.getNombreAlumno(), alum.getApellidoAlumno(), naci, ant});
        }

        tx.commit();

    }

    public int edad(String fecha_nac) {     //fecha_nac debe tener el formato dd/MM/yyyy

        Date fechaActual = new Date();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        String hoy = formato.format(fechaActual);
        String[] dat1 = fecha_nac.split("/");
        String[] dat2 = hoy.split("/");
        int anos = Integer.parseInt(dat2[2]) - Integer.parseInt(dat1[2]);
        int mes = Integer.parseInt(dat2[1]) - Integer.parseInt(dat1[1]);
        if (mes < 0) {
            anos = anos - 1;
        } else if (mes == 0) {
            int dia = Integer.parseInt(dat2[0].toString()) - Integer.parseInt(dat1[0].toString());
            if (dia > 0) {
                anos = anos - 1;
            }
        }
        return anos;
    }

