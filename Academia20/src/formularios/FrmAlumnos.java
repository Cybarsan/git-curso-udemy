    private void jTableAlumnosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAlumnosMouseClicked
        int fila = jTableAlumnos.getSelectedRow();

        veralumno = Integer.parseInt(jTableAlumnos.getValueAt(fila, 0).toString());

        tx = em.getTransaction();
        tx.begin();
        Alumnos alum = em.find(Alumnos.class, veralumno);
        al = alum;

        txtNombres.setText(alum.getNombreAlumno().trim());
        txtApellidos.setText(alum.getApellidoAlumno().trim());
        txtCedula.setText(alum.getCedulaAlumno());
        txtCelular.setText(alum.getCelular1().trim());
        txtEmail.setText(alum.getMail().trim());
        txtCelular2.setText(alum.getCelular2().trim());
        txtDireccion.setText(alum.getDireccion().trim());
        String naci = null;
        if (alum.getFechanac() != null) {
            naci = formatofec.format(alum.getFechanac());
        }
        txtFechaNacimiento.setText(naci);
        tx.commit();
        jBtnGuardar.setEnabled(true);
        defaultTableModelParientes();
        cargaParientes();
    }//GEN-LAST:event_jTableAlumnosMouseClicked

